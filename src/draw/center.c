#include "draw/center.h"

t_point3		*center(int w, int h)
{
	t_point3	*t;
	t_point2	s;

	s = point2(1000, 1000);

	t = p_point3(
		(s.x - (w - 1) * (s.x * 0.8) / (((w > h) ? w : h) - 1)) / 2,
		(s.y - (h - 1) * (s.y * 0.8) / (((w > h) ? w : h) - 1)) / 2,
		(s.y * 0.8) / (((w > h) ? w : h) - 1)
	);

	return (t);
}
