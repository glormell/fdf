#include "draw/draw_proj.h"

t_point2c		proj(t_point3 p)
{
	t_point2c	n;

	n = point2c(
		p.x,//(p->x - p->y) * cos(1 * M_PI / 180),
		p.y,//(p->x + p->y) * sin(1 * M_PI / 180) - p->z,
		white()
	);

	return (n);
}
